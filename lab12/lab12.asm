; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; R0 - register used for character output
; R1 - (no longer used - OUT trap corrupts this register)
; R2 - (no longer used - protected against potential trap corruption)
; R3 - contains the font row data currently being processed
; R4 - counter for the inner loop (8 bits per row)
; R5 - counter for the outer loop (16 rows per character)
; R6 - points to the current address within the FONT_DATA table
; Note: Characters are loaded fresh from memory (x5000/x5001) before each print
;       to avoid corruption from TRAP routines that may modify R1/R2

				
		.ORIG   x3000          
        ; Note: We no longer pre-load R1 and R2 since TRAP OUT can corrupt registers.
        ; Instead, we load the 0-bit and 1-bit characters fresh from memory each time.
       
	   
        LD      R6, CHAR_PTR        ; load the address x5002 into R6
        LDR     R6, R6, #0          ; read the ASCII value from x5002 into R6
      
        ADD     R6, R6, R6          ; R6 = R6 × 2
        ADD     R6, R6, R6          ; R6 = R6 × 2
        ADD     R6, R6, R6          ; R6 = R6 × 2
        ADD     R6, R6, R6          ; R6 = R6 × 2
        
     
        LD      R0, FONT_PTR        ; R0 gets the starting address of the font table
        ADD     R6, R6, R0          ; R6 points to FONT_DATA + (ASCII × 16)
        
       
        AND     R5, R5, #0          ; clear R5 to initialize the counter
        ADD     R5, R5, #-16        ; initialize R5 to -16 for 16 iterations
       
OUTER_LOOP
        LDR     R3, R6, #0          ; load the current row's font data into R3
        AND     R4, R4, #0          ; clear R4 to initialize the counter
        ADD     R4, R4, #-8         ; initialize R4 to -8 for 8 bits per row

INNER_LOOP 
        ADD     R3, R3, #0          ; update condition codes based on R3
        BRn     PRINT_ONE           ; if the sign bit is set, branch to print the "1" symbol
        ; Load 0-bit character fresh from memory (R1 may be corrupted by OUT trap)
        LD      R0, x5000_PTR       ; load the address x5000 into R0
        LDR     R0, R0, #0          ; read the 0-bit character directly into R0
        BRnzp   PRINT               ; unconditionally branch to the print routine

PRINT_ONE
       ; If the bit is one, output the symbol stored at x5001
       ; Load 1-bit character fresh from memory (R2 may be corrupted by OUT trap)
        LD      R0, x5001_PTR       ; load the address x5001 into R0
        LDR     R0, R0, #0          ; read the 1-bit character directly into R0

PRINT
       
        OUT                         ; TRAP x21: output the character in R0
        ADD     R3, R3, R3          ; shift left by one bit
        ADD     R4, R4, #1          ; increment the bit counter
        BRn     INNER_LOOP          ; repeat inner loop while bits remain
        LD      R0, NEXT_ROW        ; load the newline character
        OUT                         ; move output to the next line
        ADD     R6, R6, #1          ; increment the font data pointer
        ADD     R5, R5, #1          ; increment the row counter
        BRn     OUTER_LOOP          ; repeat outer loop while rows remain
        HALT                        ; HALT

x5000_PTR       .FILL   x5000       ; address of the symbol used for zero bits
x5001_PTR       .FILL   x5001       ; address of the symbol used for one bits
CHAR_PTR        .FILL   x5002       ; address holding the ASCII character to render
FONT_PTR        .FILL   FONT_DATA   ; address of the font data table
NEXT_ROW        .FILL   x000A       ; line feed character for row separation

			



